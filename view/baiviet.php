        <h3>2. Giới thiệu sơ qua về Máy tính laptop hay máy vi tính xách tay</h3>

        <p>Đặc điểm thiết kế : Tuy có đầy đủ các chức năng cơ bản như các máy tính cá nhân thường, nhưng máy tính xách tay với một 
            không gian nhỏ gọn nên các đặc điểm sau có sự khác biệt so với các máy tính cá nhân. Chi tiết hơn về từng thiết bị ở các 
            hệ thống máy tính laptop :</p>

        <p>Hệ điều hành (OS) : là tối quan trọng phải có trong các Máy tính laptop. Nó bổ nhiệm quản lý các chương trình hoặc 
            ứng dụng trong máy tính.</p>

        <p>Bộ xử lý được thiết kế riêng với sự chú trọng vào tiết kiệm năng lượng và hiệu năng, chúng có thể thay đổi tốc độ làm việc
             tuỳ theo yêu cầu của hệ thống. Để hạ giá thành sản phẩm, một số ít máy tính xách tay cũng được trang bị các bộ xử lý của 
             máy tính cá nhân để bàn.</p>

        <p>Ram : Máy tính laptop sử dụng loại RAM (So-DIMM) dành riêng, chúng ngắn hơn (và thường rộng hơn) các thanh RAM (Long-DIMM)
             dành cho máy tính cá nhân để bàn. Một máy tính laptop thường được thiết kế hai khe cắm RAM (mà thường thì khi sản xuất chúng
              chỉ được gắn RAM trên một khe để người dùng có thể dễ dàng nâng cấp).</p>

        <p>Ổ đĩa cứng : của máy tính laptop là loại ổ (2,5″) có kích thước nhỏ hơn các ổ cứng của máy tính thông thường (3,5″), máy tính 
            laptop có thể sử dụng SATA hoặc sử dụng giao tiếp ATA truyền thống trong các máy sản xuất gần đây.</p>

        <p>Chức năng đồ hoạ : Thường được tích hợp trên các chipset hoặc tích hợp trên bó mạch chủ . Đa phần các máy tính xách tay phổ 
            thông và tầm trung sử dụng bộ nhớ đồ hoạ chia sẻ từ ram hệ thống và sử dụng chức năng đồ hoạ tích hợp trên chipset. Các máy 
            tính xách tay cao cấp bộ xử lý đồ hoạ có thể được tách rời và gắn trực tiếp trên bo mạch chủ, chúng có thể sử dụng RAM riêng 
            hoặc dùng một phần RAM của hệ thống.</p>

        <p>Màn hình: máy tính laptop ngày nay luôn thuộc loại màn hình tinh thể lỏng, chúng được gắn trực tiếp vào thân máy và không 
            tách rời được. Một số máy tính laptop thiết kế màn hình quay được và gập lại che đi bàn phím - kết hợp với thể loại này thường
             là màn hình cảm ứng. Hiện giờ các hãng đã chế tạo được loại máy tính laptop có thể tháo rời màn hình, nhưng hiện chưa phổ biến
              lắm và giá khá đắt.</p>

    
